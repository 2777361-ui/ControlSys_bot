"""
Сервис для работы с текстом.
Содержит логику форматирования ответов эхобота (без привязки к Telegram).
"""


def echo_text(raw: str) -> str:
    """
    Возвращает текст «как эхо»: то, что прислали, отдаём обратно.
    Пустую строку заменяем на подсказку.
    """
    stripped = (raw or "").strip()
    if not stripped:
        return "Напиши что-нибудь — я повторю."
    return stripped
